
// function expression is a way to define a function by assigning it to a variable. Unlike a function declaration

/*
//----------- Syntax ------------ 
const myFunction = function() {
    // function body
};
  In This Syntax ---
 myFunction, is a variable holding a reference to the anonymous function.
The function itself is called a function expression because it's created and assigned to a variable.
*/


